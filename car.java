public class Car{
	private String modelName;
	private int vin;
	private int speed = 0;

	public String getModelName()
	{
		return modelName;
	}

	public void setmodelName(int modelName)
	{
		this.modelName = modelName;
	}

	public int getVin()
	{
		return vin;
	}

	public void setVin(int vin)
	{
		this.vin = vin;
	}

	public int getSpeed()
	{
		return speed;
	}

	public void setSpeed(int speed)
	{
		this.speed = speed;
	}
}